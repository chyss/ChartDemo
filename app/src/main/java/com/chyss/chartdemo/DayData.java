package com.chyss.chartdemo;

/**
 * @author chyss
 */

public class DayData
{
    private float open;
    private float curent;
    private float small;
    private float big;

    public float getBig()
    {
        return big;
    }

    public void setBig(float big)
    {
        this.big = big;
    }

    public float getOpen()
    {
        return open;
    }

    public void setOpen(float open)
    {
        this.open = open;
    }

    public float getCurent()
    {
        return curent;
    }

    public void setCurent(float curent)
    {
        this.curent = curent;
    }

    public float getSmall()
    {
        return small;
    }

    public void setSmall(float small)
    {
        this.small = small;
    }
}
